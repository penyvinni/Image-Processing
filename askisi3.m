I=imread('oIumj.png');  % εισαγωγη εικονας
I_1=fft2(I);  % μετασχηματισμος fourier
I_2=fftshift(I_1);  % ολισθηση του μετασχηματισμου στο κεντρο
 
figure, imshow(I);
figure, imshow(uint16(I_2));  % μετατροπη της εικονας σε 16-bit

% μηδενισμος των 2 περιοχων που βρισκονται οι κορυφες της εικονας
I_2(90:110,110:130)=0;  
I_2(210:230,110:130)=0;
 

I_3=ifftshift(I_2);  % αντιστροφη της ολισθησης
I_4=ifft2(I_3);   % αντιστροφη του fourier
figure,imshow(uint8(I_4))
